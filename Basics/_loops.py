# WHILE LOOP
print("---WHILE LOOP---")
i = 0
while i < 5:
    print(i)
    i+=1

# FOR LOOP
print("---FOR LOOP---")
for i in range(10):
    print(i)

# DESIGN
print("---PYRAMID---")
for i in range(1,6):
    print(i*'*')

# DESIGN REVERSED
print("---REVERSED PYRAMID---")
for i in range(5,0,-1):
    print(i*'#')